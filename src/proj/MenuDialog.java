    private void addUpt_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUpt_btnActionPerformed
        // TODO add your handling code here:
        
        Map<String, String> menu_map = new HashMap<>();
        
        
        menu_map.put("m_idx",m_idx);
        
        // 이름
        String m_name = m_name_tf.getText().trim();
        if(m_name.length()==0){
            JOptionPane.showMessageDialog(f,"메뉴명을 입력해주세요.");
            return;
        }
        menu_map.put("m_name",m_name);
        
        //카테고리
        String category_idx = String.valueOf(m_cate_cb.getSelectedIndex()+1);
        menu_map.put("category_idx",category_idx);
        
        //가격
        String price = m_price_tf.getText().trim();
        if(price.length()==0){
            JOptionPane.showMessageDialog(f,"가격을 입력해주세요.");
            return;
        }
        menu_map.put("price",price);
        
        //단가
        String unit_price = unit_price_tf.getText().trim();
        if(unit_price.length()==0){
            JOptionPane.showMessageDialog(f,"단가를 입력해주세요.");
            return;
        }
        menu_map.put("unit_price",unit_price);
        
        //시간
        String brew_time = m_time_tf.getText().trim();
        if(brew_time.length()==0){
            JOptionPane.showMessageDialog(f,"조리시간을 입력해주세요.");
            return;
        }
        menu_map.put("brew_time",brew_time);
        
        //이미지
        String img_url = m_imgUrl_tf.getText().trim();
        if (img_url.length() < 13 || !img_url.substring(0, 12).equals("/images/menu/"))
            menu_map.put("img_url", "/images/menu/noimg.png");
        else
            menu_map.put("img_url",img_url);
        
        //설명
        String desc = m_desc_tf.getText().trim();
        menu_map.put("desc",desc);
        
        //재고
        String stock = (m_stock_tf.getText().trim());
        if(stock.length() ==0){
            stock = "0";
        }
        
        switch(type){
            case 0: 
                    menu_map.put("stock",String.valueOf(stock));
                    if(f.add_menu(menu_map) == 1){
                        JOptionPane.showMessageDialog(this, "해당 메뉴가 추가되었습니다.");
                        f.startProgram();
                        this.dispose();
                    }
                break;
            case 1:
                    String addStock = m_add_stock_tf.getText().trim();
                    if(addStock.length() == 0){
                        menu_map.put("stock",String.valueOf(stock));
                    } else {
                        stockAdd(addStock);
                        
                        int stocks = Integer.parseInt(addStock)+Integer.parseInt(stock);
                        menu_map.put("stock",String.valueOf(stocks));
                    }
                    
                    if(f.update_menu(menu_map) == 1){
                        JOptionPane.showMessageDialog(this, "해당 메뉴가 수정되었습니다.");
                        this.dispose();
                    }
                break;
        }
        
        

    }//GEN-LAST:event_addUpt_btnActionPerformed

