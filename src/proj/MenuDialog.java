    public void stockAdd(String add_stock){
        SqlSession ss = Main.factory.openSession();
                        
        Map<String, String> stock_map = new HashMap<>();
        stock_map.put("m_idx", m_idx);
        stock_map.put("add_stock", add_stock);
        stock_map.put("crt_dtm",ss.selectOne("stock.last_crtDtm",m_idx));
        
        
        int res = ss.insert("stock.update", stock_map);
        if(res == 1){
            ss.commit();
            System.out.println("재고관리 수정 완료.");
        }
        
        
    }
