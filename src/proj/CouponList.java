    // 쿠폰 리스트 화면 구성
    private void setUI() {
        for(int i =0; i< f.coupon_list.size(); i++) {
            CouponVO cvo = f.coupon_list.get(i).cvo;
            CouponListPanelVO cpvo = new CouponListPanelVO(cvo);
            cpvo_list.add(cpvo);
            bg.add(cpvo.jrb);
            p_Coupon.add(cpvo.jrb);
        }
    }
    
