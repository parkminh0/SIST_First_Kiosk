    // 쿠폰 사용하기 버튼
    private void btnUseCouponActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUseCouponActionPerformed
        // 라디오버튼 텍스트 가져오기
        Enumeration<AbstractButton> tmp = bg.getElements();
        String s = null;
        while (tmp.hasMoreElements()){
            AbstractButton ab = tmp.nextElement();
            JRadioButton jb = (JRadioButton)ab;
            if (jb.isSelected())
                s = jb.getText();
        }
        
        // 선택한 쿠폰 정보 가져온 후 mainform 변수 세팅
        for (int i = 0; i < cpvo_list.size(); i++){
            CouponListPanelVO cpvo = cpvo_list.get(i);
            if (cpvo.cp_vo.getCoupon_name().equals(s)){
                mainform.coupon_index = cpvo.cp_vo.getCoupon_idx();
                mainform.discountPrice = cpvo.cp_vo.getDiscount();
                mainform.setPrice();
                mainform.isCoupon = 1;
                mainform.user_index = f.u_idx;
                break;
            }
        }
        
        dispose();
    }//GEN-LAST:event_btnUseCouponActionPerformed

